#!/usr/bin/python3

print("Give me the first number :")
x = int(input())
print("Give me the second number :")
y = int(input())
print("Thank you!")
addition = x + y
subtraction = x - y
division = x / y
multiplication = x * y
print("%d + %d = %d"%(x,y,addition))
print("%d - %d = %d"%(x,y,subtraction))
print("%d / %d = %d"%(x,y,division))
print("%d * %d = %d"%(x,y,multiplication))